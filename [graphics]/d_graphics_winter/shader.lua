function unloadTextures()
	for k,v in pairs(getElementsByType('shader', resourceRoot)) do
		destroyElement(v)
	end
	for k,v in pairs(getElementsByType('texture', resourceRoot)) do
		destroyElement(v)
	end
end

local timer
local timer_step = 20

function timerOff()

	if isTimer(timer) then 

		killTimer(timer)

	end

end

function loadTextures()

	unloadTextures()
	local counter = 1

	timer = setTimer(function()

		for i = counter, counter+timer_step  do 

			if not textures[i] then 
				return timerOff()
			end
	
			local data = textures[i]
	
			local texture = dxCreateTexture( 'img/' .. data.filepath .. '.dds', 'dxt1' )
			local shader = dxCreateShader( 'shader.fx', 0, 200, true )
	
			if isElement( texture ) then

				dxSetShaderValue( shader, 'gTexture', texture )
				engineApplyShaderToWorldTexture( shader, data.filepath )

				for _, tex_name in pairs( data.include or {} ) do
					engineApplyShaderToWorldTexture( shader, tex_name )
				end

			end
	
		end 

		counter = counter + timer_step
		
	end, 100, 0)
	
end

function updateTextures(state)

	if state then
		loadTextures()
	else
		unloadTextures()
	end

end

addEventHandler("onClientResourceStart", resourceRoot, function()
	updateTextures(true)
end) 