addCommandHandler('wheelspos_resetconfig', function()
    
    if exports.acl:isAdmin(localPlayer) then

        local coords = { 0, 0, 3, 0, 0, 0 }

        local vehicles = exports.vehicles_main:getVehiclesList()

        local index = 1

        for model, data in pairs( vehicles ) do

            setTimer(function( model, data )

                local x,y,z,rx,ry,rz = unpack(coords)

                local vehicle = createVehicle( model, x,y,z,rx,ry,rz )

                setTimer(function()

                	overridedWheelsHide[ vehicle.model ] = {
						wheel_rf_dummy = { getVehicleComponentPosition( vehicle, 'wheel_rf_dummy' ) },
						wheel_lf_dummy = { getVehicleComponentPosition( vehicle, 'wheel_lf_dummy' ) },
						wheel_rb_dummy = { getVehicleComponentPosition( vehicle, 'wheel_rb_dummy' ) },
						wheel_lb_dummy = { getVehicleComponentPosition( vehicle, 'wheel_lb_dummy' ) },
                	}

                    destroyElement(vehicle)

                end, 1000, 1)

            end, index*2000, 1, model, data)

            index = index + 1

        end

        setTimer(function()
            setClipboard( inspect( overridedWheelsHide ) )
        end, index*2000, 1)

    end

end)



