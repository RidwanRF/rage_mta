	function processBlipGroup( group )

		if #group <= 1 then return end

		local cx,cy = getMiddleBlipPoint( group )

		local r_step = 360 / #group
		local cur_r = 0

		for _, blip in pairs( group ) do

			local bx,by = getElementPosition( blip )
			local nx,ny = getPointFromDistanceRotation( cx,cy, defaultblipSize*2, -cur_r+180 )

			blip:setData('offset', { nx-bx,ny-by })
			blip:setData('size', 0.5)

			cur_r = cur_r + r_step

		end

	end

